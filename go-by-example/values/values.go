package main

import "fmt"

func main() {
	fmt.Println("go" + "lang") // string concatenation

	fmt.Println("1+1 =", 1+1) // integer arithmetic
	fmt.Println("7.0/3.0 =", 7.0/3.0) // floating point arithmetic

	// boolean logical operators
	fmt.Println(true && false)
	fmt.Println(true || false)
	fmt.Println(!true)
}
